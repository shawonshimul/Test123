
public class loops {

	public static void main(String[] args) {
		
		//for loops
		//while loops
		
		
String myName = "ProSmart";
		
		for (int i = 1; i<=10; i++) //i++ = i=i+1
			{System.out.println(myName+i);}
		
		
		int mySum = 100;
		
		for (int j = 8; j<=10; j++) //j++ = j=j+1
			{mySum = mySum+2;}
		System.out.println(mySum);
			
		
		//While
		int l = 1; //initialization
		while(l<=10) {
			//System.out.println(myName+" "+l);
			l++; //l=l+1
		}
		
		
	}

}
